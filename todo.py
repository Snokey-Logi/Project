def main():
    print("Добро пожаловать в To-Do List App!")
    while True:
        print("\n1. Добавить задачу")
        print("2. Показать все задачи")
        print("3. Отметить задачу как выполненную")
        print("4. Выйти")
        choice = input("Выберите действие: ")

        if choice == '1':
            # Заглушка для Участника 1
            print("Функция добавления задачи будет здесь.")
        elif choice == '2':
            # Заглушка для Участника 2
            print("Функция показа задач будет здесь.")
        elif choice == '3':
            # Заглушка для Участника 3
            print("Функция отметки задачи будет здесь.")
        elif choice == '4':
            break
        else:
            print("Неверный ввод.")

if __name__ == "__main__":
    main()
